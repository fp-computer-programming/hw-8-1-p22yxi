def count_score():
    a1 = input("How many one pointers are there? ")
    a2 = input("How many two pointers are there? ")
    a3 = input("How many three pointers are there? ")
    b1 = int(a1) * 1
    b2 = int(a2) * 2
    b3 = int(a3) * 3
    total = b1 + b2 + b3
    print("this team get", total, "points")


count_score()
